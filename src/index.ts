export const keysStartingWith = (obj: { [key: string]: string }, start: string) =>
  Object.keys(obj).filter((key) => key.startsWith(start));

export const checkTextWithAutoSuggestions = (text: string) => {
  const words = text && text.split(' ');
  const newText: string[] = [];
  if (words) {
    words.forEach((word) => {
      let w = word;
      if (word in emojiMap) {
        w = emojiMap[word];
      } else {
        const emojiArray = keysStartingWith(emojiMap, word);
        if (emojiArray.length === 1) {
          w = emojiMap[emojiArray[0]];
        }
      }
      newText.push(w);
    });
  }
  return newText.join(' ');
};

